package com.jinwoo.ex;

public class Rectangle {
	//data
	private int width;
	private int height;
	//method
	public int area() {
		return width*height;
	}
	//setter
	public void setWidth(int width) {
		this.width = width;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	//getter
	public int getWidth() {
		return width = width;
	}
	public int getHeight() {
		return height = height;
	}
}
